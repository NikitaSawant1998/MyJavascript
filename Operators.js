// +, -, *, /, %, ++, --
let a=5;
let b=101;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(b/a);
console.log(b%a);
console.log(a++);
console.log(b++);
console.log(++a);
console.log(++b);

//Comparison: == (checks value), === (checks value + type), !=, !==, <, >, <=, >=
let c="5";
let d=5;
console.log(a==b);
console.log(a==c);
console.log(a==d);
console.log(a===b);
console.log(a===c);
console.log(a===d);
console.log(a<b);
console.log(a>=b);
console.log(a!==b);

//Logical: && (AND), || (OR), ! (NOT)
